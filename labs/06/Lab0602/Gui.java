    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int tiempoMilisegundos = 0;
        switch (jComboBox1.getSelectedIndex()) {
            case 0:
            tiempoMilisegundos = 0;
            break;
            case 1:
            tiempoMilisegundos = 500;
            break;
            case 2:
            tiempoMilisegundos = 100;
            break;
            default:
            tiempoMilisegundos = 0;
        }
        ima1 = new imagen1(tiempoMilisegundos, jLabel1);
        ima2 = new imagen2(tiempoMilisegundos, jLabel2);
        ima3 = new imagen3(tiempoMilisegundos, jLabel3);

        respuesta1 = false;
        respuesta2 = false;
        respuesta3 = false;
        jButton1.setEnabled(false);
        ima1.start();
        ima2.start();
        ima3.start();
    }//GEN-LAST:event_jButton1ActionPerformed

