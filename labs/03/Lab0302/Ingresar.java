
import java.util.Date;


public class Ingresar {
    private String placa;
    private String tipo;
    private Date fechaHoraIngreso;

    public Ingresar(String placa, String tipo, Date fechaHoraIngreso) {
        this.placa = placa;
        this.tipo = tipo;
        this.fechaHoraIngreso = fechaHoraIngreso;
    }
}